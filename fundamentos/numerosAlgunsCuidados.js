console.log(7 / 0); // -> caso divida algum número por "0" vai dar infinito 
console.log("10" / 2); // -> na divisão se for numero inteiro ele aceita mesmo sendo uma string
console.log("3" + 2); // -> na soma ele não vai somar ele vai concatenar ou seja juntar pois o numero estra entre parenteses ou seja se tornou uma string
console.log("3" - 2); // na subtração ele vai diminuir mesmo sendo uma string
console.log("Show" * 2); // -> não tem como multiplicar a string
console.log(0.1 + 0.7); // -> operações com ponto flutuante vão ser um pouco imprecisas retornando números aproximados
console.log((10.345).toFixed(2)) // -> console.log(10.toFixed(2)) não funciona pois tem que colocar o numero antes entre parenteses
